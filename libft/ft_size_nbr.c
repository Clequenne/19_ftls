#include "includes/libft.h"

int		ft_size_nbr(long long int n)
{
	long long int i;

	i = 1;
	if (n < 0)
		n *= -1;
	while (n >= 10)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
